using System.Collections.Generic;

namespace YXH.Model
{
    /// <summary>
    /// 程序文件版本信息
    /// </summary>
    public class FileVersionInfo
    {
        /// <summary>
        /// 文件名称
        /// </summary>
        public string FileName;
        /// <summary>
        /// 目录路径
        /// </summary>
        public string DirPath;
        /// <summary>
        /// 服务器路径
        /// </summary>
        public string ServerPath;
        /// <summary>
        /// 文件的MD5编码
        /// </summary>
        public string MD5;
    }
}
